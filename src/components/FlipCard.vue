<template>
  <div class="FlipCard">
    <div class="flip-card" @click="isFlipped = !isFlipped">
      <div class="flip-card-inner" :class ="{flipped: isFlipped}">
        <div class="flip-card-front">
          <p id="txt">{{ front }}</p>
        </div>
        <div class="flip-card-back">
          <p id="txt">{{ back }}</p>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: ["flipcard"],

  data() {
    return {
      front: this.flipcard[0],
      back: this.flipcard[1],
      isFlipped: false,
    }
  }
};

</script>


